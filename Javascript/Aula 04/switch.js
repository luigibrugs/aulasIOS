// Exemplo 6: Uso do switch (Notas de avaliação)

let estrelas = 5;

switch (estrelas) {
    case 1:
        console.log('Horrível seu lixo 💩')
        break;

    case 2:
        console.log('Ainda está ruim 😔')
        break;

    case 3:
        console.log('Dá para melhorar 🙄')
        break;

    case 4:
        console.log('Tá ficando bom 😎')
        break;

    case 5:
        console.log('Perfeito ❤️')
        break;

    default:
        console.log('Por favor, insira uma nota de 1 a 5.')
        break;
}

